const STORAGE = 'nexa.lang';

@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly _lang = signal<Lang>(this.read());
  readonly lang = this._lang.asReadonly();
  readonly dict = computed(() => ({ ...DICT[this._lang()], ...TENANT_TRANSLATIONS[this._lang()] }));

  set(lang: Lang): void {
    this._lang.set(lang);
    try { localStorage.setItem(STORAGE, lang); } catch { /* ignore */ }
  }

  t(key: string, vars?: Record<string, string | number>): string {
    let str = this.dict()[key] ?? key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
      }
    }
    return str;
  }

  private read(): Lang {
    try {
      const raw = localStorage.getItem(STORAGE);
      return raw === 'es' ? 'es' : 'en';
    } catch {
      return 'en';
    }
  }
}
